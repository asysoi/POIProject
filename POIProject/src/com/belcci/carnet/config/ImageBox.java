package com.belcci.carnet.config;

public class ImageBox {
	private float xl;
	private float yl;
	private float xr;
	private float yr;
	private int border;
	private float widthBorderLine;
	private String filename;
	
	public float getXl() {
		return xl;
	}
	public void setXl(float xl) {
		this.xl = xl;
	}
	public float getYl() {
		return yl;
	}
	public void setYl(float yl) {
		this.yl = yl;
	}
	public float getXr() {
		return xr;
	}
	public void setXr(float xr) {
		this.xr = xr;
	}
	public float getYr() {
		return yr;
	}
	public void setYr(float yr) {
		this.yr = yr;
	}
	public int getBorder() {
		return border;
	}
	public void setBorder(int border) {
		this.border = border;
	}
	public float getWidthBorderLine() {
		return widthBorderLine;
	}
	public void setWidthBorderLine(float widthBorderLine) {
		this.widthBorderLine = widthBorderLine;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}

}
